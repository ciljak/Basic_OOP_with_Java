    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:  here we save our object
        if (gui_created_school != null) { // object was created and is not empty
             if (supportFunctionOnGUI.saveObject(GUI_SAVE_FILE_NAME, gui_created_school)) { //
                 System.out.println("School object succcesfully saved to file named: " + GUI_SAVE_FILE_NAME);
                 jLabel8.setText("School object succcesfully saved to file named: " + GUI_SAVE_FILE_NAME);  // main message text
             } else {
                 System.out.println("There were errors during saving process!");
             } 
        
      
            
            
            
        } else { // object does not exist
            jLabel5.setText("");  // first line of mesage
            jLabel6.setText("");  // second line of message
            jLabel7.setText("");  // third line of message
            jLabel8.setText("Object can not be saved beacause is empty, please create them first!");  // main message text
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here: part responsible for reading and showing last saved object
        
        gui_created_school = (School)supportFunctionOnGUI.readObject(GUI_SAVE_FILE_NAME);
        System.out.println("Our restored school from file contains these data:");
        
        if (gui_created_school != null) { // object was created and is not empty
             jLabel5.setText("School name is: " + gui_created_school.getName());  // first line of mesage
            jLabel6.setText("School type: " + gui_created_school.getType());  // second line of message
            jLabel7.setText("Number of classes is: " + gui_created_school.getNrOfClasses());  // third line of message
            jLabel8.setText("We succesfully restored school named: "  + gui_created_school.getName());  // main message text  
            
            // restore texts into a text editation fields
            jTextField1.setText(gui_created_school.getName());
            jTextField2.setText(gui_created_school.getType());
            jTextField3.setText(String.valueOf(gui_created_school.getNrOfClasses()));
      
            
            
            
        } else { // object does not exist and cannot be deserialized
            jLabel5.setText("");  // first line of mesage
            jLabel6.setText("");  // second line of message
            jLabel7.setText("");  // third line of message
            jLabel8.setText("Object can not be restored beacause is empty, please create them  and save first!");  // main message text
        }
        
        try {
           gui_created_school.showAllSchoolClasses(); // show content of restored school
        }
	catch(NullPointerException e) {
	   System.out.println("NullPointerException thrown!");
	}
    }//GEN-LAST:event_jButton3ActionPerformed

