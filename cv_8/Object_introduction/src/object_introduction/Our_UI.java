    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here: Create object School
        
        String schoolName = null, schoolType = null;
        
        // read from field
        if (jTextField1.getText() == "") {
            jLabel8.setText("Please fill school name!");  // main message text
        } else {
            schoolName = jTextField1.getText();
        }
        
        
        if (jTextField2.getText() == "") {
            jLabel8.setText("Please fill school type!");  // main message text
        } else {
            schoolType = jTextField2.getText();
        }
        
        int numberOfClasses = Integer.parseInt( jTextField3.getText());
        
        if ( (schoolName.isEmpty()) || (schoolType.isEmpty())) { // testing if it si not empty
            
            jLabel5.setText("");  // first line of mesage
            jLabel6.setText("");  // second line of message
            jLabel7.setText("");  // third line of message
            jLabel8.setText("Please correct values entered into a text field!");  // main message text
            
        } else {
            
            
            gui_created_school = new School(schoolName, schoolType, numberOfClasses);
             // outputting text after scuccesfull object creation
            jLabel5.setText("School name is: " + schoolName);  // first line of mesage
            jLabel6.setText("School type: " + schoolType);  // second line of message
            jLabel7.setText("Number of classes is: " + numberOfClasses);  // third line of message
            jLabel8.setText("Object gui_created_school has been created!");  // main message text
        }
        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

